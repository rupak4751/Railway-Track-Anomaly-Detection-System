# --- 1. Configuration and Paths ---
IMAGE_SIZE = (224, 224) # Standard input size for ResNet50
BATCH_SIZE = 32
NUM_CLASSES = 2 # Defect, Non Defect (or vice versa based on your labeling)
EPOCHS = 20 # Start with a reasonable number, early stopping will prevent overfitting
LEARNING_RATE = 0.0001 # Start with a small learning rate for transfer learning

# Define your dataset paths
# Ensure these paths correctly point to your 'train' and 'test' folders
# and that 'train' and 'test' each contain 'Defect' and 'Non Defect' subfolders.
TRAIN_DIR = "/content/drive/MyDrive/Railway_Fault_Dataset" # e.g., 'C:/Users/YourUser/Desktop/Railway_Fault_Dataset/train'
TEST_DIR = "/content/drive/MyDrive/Railway_Fault_Dataset_Test" # e.g., 'C:/Users/YourUser/Desktop/Railway_Fault_Dataset_Test/test'


# --- 2. Data Loading and Augmentation ---

# Data Augmentation for Training
# Apply various transformations to make the model robust
train_datagen = ImageDataGenerator(
    rotation_range=20,        # Rotate images by 20 degrees
    width_shift_range=0.1,    # Shift images horizontally by 10%
    height_shift_range=0.1,   # Shift images vertically by 10%
    shear_range=0.1,          # Shear intensity
    zoom_range=0.1,           # Random zoom
    horizontal_flip=True,     # Randomly flip images horizontally
    fill_mode='nearest',      # Fill pixels that appear after transformations
    preprocessing_function=tf.keras.applications.resnet50.preprocess_input # ResNet specific preprocessing
)

# No augmentation for validation/test data, only resizing and preprocessing
test_datagen = ImageDataGenerator(
    preprocessing_function=tf.keras.applications.resnet50.preprocess_input
)

# Load data from directories
# class_mode='binary' for two classes (0 or 1)
train_generator = train_datagen.flow_from_directory(
    TRAIN_DIR,
    target_size=IMAGE_SIZE,
    batch_size=BATCH_SIZE,
    class_mode='binary', # Assumes 'Defect' and 'Non Defect' are your folder names
    shuffle=True
)

test_generator = test_datagen.flow_from_directory(
    TEST_DIR,
    target_size=IMAGE_SIZE,
    batch_size=BATCH_SIZE,
    class_mode='binary',
    shuffle=False # Do not shuffle test data to maintain order for evaluation
)

# Get class indices from the generator to calculate class weights
# This assumes 'Defect' maps to 0 and 'Non Defect' maps to 1 based on alphanumeric order of folder names.
# Verify this by printing train_generator.class_indices if needed.
class_indices = train_generator.class_indices
# e.g., {'Defect': 0, 'Non Defect': 1} or {'Non Defect': 0, 'Defect': 1}
print("Class Indices:", class_indices)

# --- 3. Handle Class Imbalance ---
# Calculate class weights to give more importance to the minority class (Non Defect based on your numbers)
# This helps the model pay more attention to the under-represented class.

# In your case, 'Defect' is 1067 and 'Non Defect' is 441 in training.
# So, 'Non Defect' is the minority class.
# The calculation for class_weights typically assigns a higher weight to the minority class.
# Ensure the labels '0' and '1' correspond to 'Defect' and 'Non Defect' as per flow_from_directory.
# flow_from_directory assigns labels alphabetically by folder name.
# If your folders are 'Defect' and 'Non Defect':
#   'Defect' will be class 0, 'Non Defect' will be class 1.
#   So, you'd want to weigh class 1 higher.

class_weights = class_weight.compute_class_weight(
    class_weight='balanced',
    classes=np.unique(train_generator.classes),
    y=train_generator.classes
)
class_weights = dict(enumerate(class_weights))
print("Calculated Class Weights:", class_weights)

# --- 4. Build the Model (Transfer Learning with ResNet50) ---

# Load the ResNet50 model pre-trained on ImageNet data
# include_top=False means we don't include the original classification head
base_model = ResNet50(weights='imagenet', include_top=False, input_shape=(IMAGE_SIZE[0], IMAGE_SIZE[1], 3))

# Freeze the convolutional base layers so they are not trained
# This uses the pre-learned features as is.
for layer in base_model.layers:
    layer.trainable = False

# Add your custom classification head
x = base_model.output
x = GlobalAveragePooling2D()(x) # Reduces spatial dimensions, keeping global features
x = Dense(256, activation='relu')(x) # A dense layer
x = Dropout(0.5)(x) # Dropout for regularization to prevent overfitting
predictions = Dense(NUM_CLASSES - 1 if NUM_CLASSES == 2 else NUM_CLASSES, activation='sigmoid' if NUM_CLASSES == 2 else 'softmax')(x) # Output layer

# If binary classification (2 classes), use sigmoid and 1 neuron.
# If multi-class (>2 classes), use softmax and NUM_CLASSES neurons.
# For anomaly detection (binary), sigmoid is appropriate for a single output neuron
# that gives the probability of being the positive class (e.g., "Non Defect").
# If you configure class_mode='categorical' for 2 classes, then use softmax and 2 neurons.
# Here, assuming `class_mode='binary'` for 2 classes, so `sigmoid` and 1 neuron for `predictions`.
# Let's adjust for binary case with 1 output neuron:
if NUM_CLASSES == 2:
    predictions = Dense(1, activation='sigmoid')(x) # Single neuron for binary classification
else:
    predictions = Dense(NUM_CLASSES, activation='softmax')(x)


model = Model(inputs=base_model.input, outputs=predictions)

# --- 5. Compile the Model ---
model.compile(optimizer=Adam(learning_rate=LEARNING_RATE),
              loss='binary_crossentropy' if NUM_CLASSES == 2 else 'categorical_crossentropy',
              metrics=['accuracy',
                        tf.keras.metrics.Precision(name='precision'),
                        tf.keras.metrics.Recall(name='recall'),
                        tf.keras.metrics.AUC(name='auc')])

model.summary()

# --- 6. Train the Model ---

# Callbacks for better training control
early_stopping = EarlyStopping(monitor='val_loss', patience=10, restore_best_weights=True)
model_checkpoint = ModelCheckpoint('best_model.h5', monitor='val_loss', save_best_only=True)

history = model.fit(
    train_generator,
    epochs=EPOCHS,
    validation_data=test_generator, # Using test_generator as validation here, consider a separate validation set if possible
    class_weight=class_weights, # Apply calculated class weights
    callbacks=[early_stopping, model_checkpoint]
)

# --- 7. Evaluate the Model ---

print("\n--- Evaluating on Test Data ---")
loss, accuracy, precision, recall, auc_score = model.evaluate(test_generator)

print(f"Test Loss: {loss:.4f}")
print(f"Test Accuracy: {accuracy:.4f}")
print(f"Test Precision: {precision:.4f}")
